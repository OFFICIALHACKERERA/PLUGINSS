from ..core.managers import eor
from ..sql_helper.globals import gvarstatus
from . import hmention, legend

menu_category = "tools"


@legend.legend_cmd(
    pattern="ping( -a|$)",
    command=("ping", menu_category),
    info={
        "header": "check how long it takes to ping your userbot",
        "flags": {"-a": "average ping"},
        "usage": ["{tr}ping", "{tr}ping -a"],
    },
)
async def _(event):
    "To check ping"
    type = event.pattern_match.group(1)
    start = datetime.now()
    if type == " -a":
        legendevent = await eor(event, "`!....`")
        await asyncio.sleep(0.3)
        await legendevent.edit("`..!..`")
        await asyncio.sleep(0.3)
        await legendevent.edit("`....!`")
        end = datetime.now()
        tms = (end - start).microseconds / 1000
        ms = round((tms - 0.6) / 3, 3)
        await legendevent.edit(f"**👨‍💻 Average Pong!**\n➥ {ms} ms")
    else:
        sweetie = (
            gvarstatus("PING_IMG")
            or "https://telegra.ph/file/9fdec96f8f340b8946845.jpg"
        )
        llol = [x for x in sweetie.split()]
        IPIC = random.choice(llol)
        if sweetie == "OFF":
            legendevent = await eor(event, "(❛ ᑭσɳց ❜! ⚡", "html")
            end = datetime.now()
            ms = (end - start).microseconds / 1000
            await legendevent.edit(
                f"(❛ ᑭσɳց ❜!\n\n 💞 ms {ms}\n 💞 my master: {hmention}</b></i>",
                parse_mode="html",
            )
        else:
            legendevent = await eor(event, "(❛ ᑭσɳց ❜! ⚡", "html")
            end = datetime.now()
            ms = (end - start).microseconds / 1000
            await legendevent.delete()
            await event.client.send_file(
                event.chat_id,
                IPIC,
                caption=f"(❛ ᑭσɳց ❜!\n\n 💞 ms {ms}\n💞 my master {hmention}</b></i>",
                parse_mode="html",
            )


